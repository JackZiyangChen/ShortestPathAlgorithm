    @Override
    public boolean add(E e) {
        if(size+1==data.length)
        {
            return false;
        }
        
        
        int opening = ++size;
        
        for(;opening>1 && ((Comparable)e).compareTo(data[opening/2])<0;opening/=2){
            data[opening] = data[opening/2];
        }
        data[opening] = e;
        return true;
    }
    
